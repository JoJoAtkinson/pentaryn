# --- Subprocess fallback (kept for compatibility; in-process is preferred) ---

def _parse_flag_args(tokens: list[str]) -> dict[str, Any]:
    out: dict[str, Any] = {}
    i = 0
    while i < len(tokens):
        token = tokens[i]
        if not token.startswith("--"):
            i += 1
            continue
        key = token[2:]
        if i + 1 < len(tokens) and not tokens[i + 1].startswith("--"):
            out[key] = tokens[i + 1]
            i += 2
        else:
            out[key] = True
            i += 1
    int_keys = (
        "limit", "level", "level_min", "level_max",
        "ac_min", "ac_max", "ac_base_min", "ac_base_max", "ac_cap_dexmod",
        "passive_perception_min", "passive_perception_max",
        "strength_min", "strength_max", "dexterity_min", "dexterity_max",
        "constitution_min", "constitution_max", "intelligence_min", "intelligence_max",
        "wisdom_min", "wisdom_max", "charisma_min", "charisma_max",
        "strength_required_min", "strength_required_max",
        "range_min", "range_max",
    )
    for int_key in int_keys:
        if int_key in out and isinstance(out[int_key], str):
            try:
                out[int_key] = int(out[int_key])
            except ValueError:
                pass
    float_keys = ("cr_min", "cr_max", "cost_min", "cost_max", "weight_min", "weight_max")
    for f_key in float_keys:
        if f_key in out and isinstance(out[f_key], str):
            try:
                out[f_key] = float(out[f_key])
            except ValueError:
                pass
    # Boolean flags from CLI come in as strings; only explicit falsy values flip
    # to False (any unrecognized string stays truthy, matching the function default).
    bool_keys = (
        "dedupe", "concentration", "verbal", "somatic", "material", "material_consumed",
        "requires_attunement", "is_weapon", "is_armor", "is_light", "is_versatile",
        "is_thrown", "is_finesse", "is_two_handed", "is_subclass", "is_subspecies",
        "is_exotic", "is_secret", "ac_add_dexmod", "grants_stealth_disadvantage",
        "vector", "fuzzy", "strict",
    )
    for bool_key in bool_keys:
        if bool_key in out and isinstance(out[bool_key], str):
            out[bool_key] = out[bool_key].strip().lower() not in ("false", "0", "no", "off")
    return out


def main() -> int:
    if len(sys.argv) >= 3 and sys.argv[1] == "--mcp-tool":
        tool_name = sys.argv[2]
        handler = MCP_HANDLERS.get(tool_name)
        if handler is None:
            print(f"Error: Unknown tool: {tool_name}", file=sys.stderr)
            return 1
        try:
            single_arg_tools = {
                "get_monster_details": "key",
                "get_spell_details": "key",
                "get_magic_item": "key",
                "get_item": "key",
                "get_class_info": "key",
                "get_rule_section": "key",
                "get_background": "key",
                "get_species": "key",
                "get_feat": "key",
                "get_spell_list": "class_slug",
                "get_environment": "key",
                "get_creatureset": "key",
                "get_itemset": "key",
                "get_ruleset": "key",
            }
            if tool_name in single_arg_tools:
                if len(sys.argv) < 4:
                    print(f"Error: {tool_name} requires an argument", file=sys.stderr)
                    return 1
                kwargs = {single_arg_tools[tool_name]: sys.argv[3]}
            else:
                rest = sys.argv[3:]
                if rest and rest[0].startswith("--"):
                    kwargs = _parse_flag_args(rest)
                elif rest:
                    try:
                        kwargs = json.loads(rest[0])
                    except json.JSONDecodeError as exc:
                        print(f"Error: Invalid JSON arguments: {exc}", file=sys.stderr)
                        return 1
                else:
                    kwargs = {}
            result = handler(**kwargs)
            print(json.dumps(result, indent=2, ensure_ascii=False))
            return 0
        except requests.HTTPError as exc:
            print(f"API Error: {exc.response.status_code} - {exc.response.text}", file=sys.stderr)
            return 1
        except TypeError as exc:
            print(f"Error: Invalid arguments for {tool_name}: {exc}", file=sys.stderr)
            return 1
        except Exception as exc:
            print(f"Error: {exc}", file=sys.stderr)
            return 1

    print("Usage: srd5_2.py --mcp-tool <tool_name> [args]", file=sys.stderr)
    print(f"Available tools: {', '.join(sorted(MCP_HANDLERS.keys()))}", file=sys.stderr)
    return 1


if __name__ == "__main__":
    raise SystemExit(main())
