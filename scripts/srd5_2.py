    _add_bool(params, "vector", vector)
    _add_bool(params, "fuzzy", fuzzy)
    _add_bool(params, "strict", strict)
    if object_model:
        params["object_model"] = object_model
    if document_pk:
        params["document_pk"] = document_pk
    return _api_get("/v2/search/", params)


