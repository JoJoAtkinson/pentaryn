#!/usr/bin/env python3
"""
Open5e v2 API integration for D&D 5e SRD content (and beyond).

Provides MCP tools for monsters, spells, items, conditions, rules, backgrounds,
species, feats, and a universal cross-type search. Uses /v2/ endpoints, which
include both the 2014 SRD (srd-2014) and the 2024 SRD 5.2 (srd-2024) — the
latter is the rules basis for D&D 5.5e.

Common source keys for the `source` parameter:
  srd-2024  → 2024 SRD 5.2 (5.5e). Default choice for current play.
  srd-2014  → 2014 SRD 5.1.
  tob       → Tome of Beasts (Kobold Press).
  a5e-ag    → Adventurer's Guide (Level Up: Advanced 5E).
  open5e    → Open5e Originals.

Import as: from scripts.srd5_2 import search_monsters, search_spells, etc.

== KNOWN OPEN5E v2 QUIRKS — read before adding/modifying filters ==

The OpenAPI schema at https://api.open5e.com/schema/ is auto-generated by
Django REST framework and includes filter slots that aren't actually wired
to anything server-side. Verify behavior empirically when in doubt.

1) ?search= is COSMETIC on every per-resource endpoint. DRF auto-includes the
   parameter whenever SearchFilter is registered, but Open5e's ViewSets don't
   declare `search_fields`, so the filter is a no-op. /v2/creatures/?search=X
   returns the full unfiltered list regardless of X. The ONLY working full-
   text path is the dedicated /v2/search/ endpoint — exposed as `search_srd`.
   Use search_srd(query=..., object_model='rule'|'creature'|...) and pass
   vector=True for semantic queries ('creatures that breathe fire').

2) name__icontains is documented on SOME endpoints but not others. Endpoints
   that only declare name__contains silently drop name__icontains. See
   _ENDPOINT_NAME_FORMS for the per-endpoint map. The 9 endpoints with
   neither form (gamesystems, publishers, licenses, itemcategories,
   itemrarities, skills, itemsets, languages, documents) don't support
   partial-name match server-side at all — list-all + client-side filter.

3) Filter field names are inconsistent across endpoints:
     /v2/creatures/  → bare `type`, `size`, `challenge_rating` (NOT __key)
     /v2/magicitems/ → bare `rarity` (NOT rarity__key)
     /v2/items/      → bare `category` (NOT category__key)
     /v2/spells/     → `school__key` (NOT bare `school`)
   Each endpoint's filterset is configured by hand on the Open5e side.

4) Filter param names can differ from response field names. Example:
   `ability_score_strength__gte=20` IS honored as a filter on /v2/creatures/,
   but the field `ability_score_strength` is stripped from list responses
   (only present in /v2/creatures/<key>/ detail). When verifying a filter
   worked, compare result COUNTS against the unfiltered baseline rather than
   reading the response field.

5) Boolean filters accept Python True/False (requests serializes to 'True'/
   'False' strings, which Open5e accepts) and the strings 'true'/'false'.
   Integer 1/0 are silently rejected — sending them returns the unfiltered
   list. _add_bool() in this module always passes Python booleans.

6) /v1/spelllist/<class>/ returns v1-style slugs (e.g. 'fireball') that DO
   NOT match v2 keys (e.g. 'srd-2024_fireball'). Don't chain v1 slugs into
   v2 get_spell_details — see get_spell_list's docstring for the right path.

7) For free-text 'classes that know X' queries: search_srd(object_model='X',
   vector=True) is the working path. Per-endpoint search_* tools only do
   structured field filters (CR, level, rarity, etc.), not keyword search.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from dataclasses import dataclass
from typing import Any, Optional

import requests
from requests.adapters import HTTPAdapter
from requests_cache import CachedSession
from urllib3.util.retry import Retry


BASE_URL = "https://api.open5e.com"

_REPO_ROOT = Path(__file__).resolve().parents[1]
_CACHE_DIR = _REPO_ROOT / ".cache"
_CACHE_DIR.mkdir(parents=True, exist_ok=True)

# Bumping cache_name forces a fresh SQLite — old v1 responses are discarded
# automatically on first call after the v2 migration.
_CACHE_NAME = "srd5_2_v2"

_session: Optional[CachedSession] = None


def _get_session() -> CachedSession:
    global _session
    if _session is not None:
        return _session
    session = CachedSession(
        cache_name=str(_CACHE_DIR / _CACHE_NAME),
        backend="sqlite",
        expire_after=60 * 60 * 24 * 30,  # 30 days
        allowable_methods=("GET",),
    )
    retry = Retry(
        total=3,
        backoff_factor=0.5,
        status_forcelist=(500, 502, 503, 504),
        allowed_methods=("GET",),
    )
    adapter = HTTPAdapter(max_retries=retry)
    session.mount("https://", adapter)
    session.mount("http://", adapter)
    _session = session
    return session


def _api_get(endpoint: str, params: Optional[dict[str, Any]] = None) -> dict[str, Any]:
    """GET an open5e endpoint via the cached, retrying session."""
    url = f"{BASE_URL}{endpoint}"
    response = _get_session().get(url, params=params or {}, timeout=10)
    response.raise_for_status()
    return response.json()


# Per-endpoint name-filter forms. Mapped by EMPIRICALLY probing each endpoint
# (see module-level quirk #2). Open5e's OpenAPI schema is misleading — many
# declared filters are silently dropped server-side. Tuple is
# (partial_form, exact_form), each may be None meaning "the API has no working
# filter of that kind — callers must list-all and filter client-side".
_ENDPOINT_NAME_FORMS: dict[str, tuple[Optional[str], Optional[str]]] = {
    # Full filter support (case-insensitive substring + iexact).
    "/v2/spells/":           ("name__icontains", "name__iexact"),
    "/v2/creatures/":        ("name__icontains", "name__iexact"),
    "/v2/magicitems/":       ("name__icontains", "name__iexact"),
    "/v2/items/":            ("name__icontains", "name__iexact"),
    "/v2/rules/":            ("name__icontains", "name__iexact"),
    "/v2/backgrounds/":      ("name__icontains", "name__iexact"),
    "/v2/species/":          ("name__icontains", "name__iexact"),
    "/v2/feats/":            ("name__icontains", "name__iexact"),
    "/v2/weaponproperties/": ("name__icontains", "name__iexact"),
    # Only case-sensitive name__contains (no __icontains).
    "/v2/conditions/":       ("name__contains", "name__iexact"),
    "/v2/classes/":          ("name__contains", "name__iexact"),
    "/v2/abilities/":        ("name__contains", "name__iexact"),
    "/v2/alignments/":       ("name__contains", "name__iexact"),
    "/v2/creaturesets/":     ("name__contains", "name__iexact"),
    "/v2/creaturetypes/":    ("name__contains", "name__iexact"),
    "/v2/damagetypes/":      ("name__contains", "name__iexact"),
    "/v2/environments/":     ("name__contains", "name__iexact"),
    "/v2/sizes/":            ("name__contains", "name__iexact"),
    "/v2/spellschools/":     ("name__contains", "name__iexact"),
    "/v2/rulesets/":         ("name__contains", "name"),  # no name__iexact!
    # Exact-only: partial match silently dropped, exact case-INSENSITIVE works.
    "/v2/itemsets/":         (None, "name__iexact"),
    "/v2/languages/":        (None, "name__iexact"),
    "/v2/armor/":             (None, "name__iexact"),
    "/v2/weapons/":          (None, "name__iexact"),
    # Exact-only, case-SENSITIVE (no iexact form).
    "/v2/publishers/":       (None, "name"),
    "/v2/licenses/":         (None, "name"),
    "/v2/documents/":        (None, "name"),
    # NO working name filter at all. List-all + client-side grep. These are
    # short enums (24, 6, 3, 20 entries) so listing everything is cheap.
    "/v2/itemcategories/":   (None, None),
    "/v2/itemrarities/":     (None, None),
    "/v2/gamesystems/":      (None, None),
    "/v2/skills/":           (None, None),
}
# Default applies when an endpoint isn't in the map (defensive — won't be hit
# in normal use but keeps _build_query well-defined for ad-hoc calls).
_DEFAULT_NAME_FORMS: tuple[Optional[str], Optional[str]] = ("name__icontains", "name__iexact")


def _build_query(
    *,
    name: Optional[str] = None,
    match: str = "partial",
    source: Optional[str] = None,
    fields: Optional[str] = None,
    exclude: Optional[str] = None,
    ordering: Optional[str] = None,
    limit: int = 10,
    extra: Optional[dict[str, Any]] = None,
    endpoint: Optional[str] = None,
) -> dict[str, Any]:
    """Compose v2 query params, applied uniformly across search tools.

    Pass `endpoint` (e.g. '/v2/creatures/') so the right name-filter form is
    selected per Open5e's per-ViewSet inconsistency (see _ENDPOINT_NAME_FORMS
    and quirk #2 in the module docstring). When endpoint is None, defaults to
    name__icontains / name__iexact — works for the most common search tools."""
    params: dict[str, Any] = {"limit": limit}
    if name:
        partial_form, exact_form = _ENDPOINT_NAME_FORMS.get(endpoint or "", _DEFAULT_NAME_FORMS)
        chosen = exact_form if match == "exact" else partial_form
        if chosen is not None:
            params[chosen] = name
        # else: endpoint silently drops every name-filter form (e.g. itemcategories,
        # gamesystems). Caller gets the unfiltered list — documented per tool.
    if source:
        params["document__key__in"] = source
    if fields:
        params["fields"] = fields
    if exclude:
        params["exclude"] = exclude
    if ordering:
        params["ordering"] = ordering
    if extra:
        params.update(extra)
    return params


# Default ranking when the caller doesn't pass `source`. No filter is applied —
# all sources are searched — but results are sorted in three tiers: `prefer`
# first, `demote` last, everything else in between. Combined with dedupe-keeping-
# first, this yields "latest canonical, third-party fallback, 2014 only if
# nothing else has it" without hiding obscure-source hits behind a hard filter.
@dataclass(frozen=True)
class PrioritySpec:
    prefer: tuple[str, ...] = ()
    demote: tuple[str, ...] = ()


DEFAULT_PRIORITY_SRD = PrioritySpec(prefer=("srd-2024",), demote=("srd-2014",))
DEFAULT_PRIORITY_CONDITIONS = PrioritySpec(prefer=("core", "a5e-ag"))


def _resolve_source(
    source: Optional[str], default: PrioritySpec
) -> tuple[Optional[str], PrioritySpec]:
    """Map the caller's `source` arg into (filter, priority_spec).
    - source=None        → no filter, default priority
    - source="" / whitespace / "," → no filter, no priority sort (raw API order)
    - source="x"         → filter to x (single-source spec, sort is a no-op)
    - source="x,y"       → filter to x,y; rank x first, y second (user order wins)

    Whitespace around comma-separated keys is stripped for both the filter and
    the priority spec, so 'srd-2024, srd-2014' and 'srd-2024,srd-2014' behave
    identically (Open5e tolerates the padded form, but we send the cleaned one
    to keep filter and ranking in lockstep).
    """
    if source is None:
        return None, default
    keys = tuple(k.strip() for k in source.split(",") if k.strip())
    if not keys:
        return None, PrioritySpec()
    return ",".join(keys), PrioritySpec(prefer=keys)


def _doc_key(entry: dict[str, Any]) -> str:
    """Extract a source key from a result, handling both shapes open5e returns
    (nested {key, name, ...} object on creatures/spells; bare string on rules)."""
    doc = entry.get("document") if isinstance(entry, dict) else None
    if isinstance(doc, dict):
        return str(doc.get("key") or "")
    if isinstance(doc, str):
        return doc
    return ""


def _rank_by_spec(key: str, spec: PrioritySpec) -> tuple[int, int]:
    """3-tier rank: 0=prefer, 1=middle, 2=demote. Sub-rank preserves listed order
    within each tier so the sort is fully deterministic."""
    if key in spec.prefer:
        return (0, spec.prefer.index(key))
    if key in spec.demote:
        return (2, spec.demote.index(key))
    return (1, 0)


def _sort_by_priority(
    results: list[dict[str, Any]], spec: PrioritySpec
) -> list[dict[str, Any]]:
    """Stable sort by spec. Empty spec is a no-op (preserves API order)."""
    if not spec.prefer and not spec.demote:
        return list(results)
    return sorted(results, key=lambda item: _rank_by_spec(_doc_key(item), spec))


def _dedupe_by_name(
    results: list[dict[str, Any]],
) -> tuple[list[dict[str, Any]], list[str]]:
    """Stable dedupe keeping the first occurrence of each name (case-folded,
    trimmed). Items missing a name are kept (they can't be safely collapsed).
    Returns (kept, dropped_keys). Run *after* the priority sort so the surviving
    entry is the highest-priority one."""
    seen: set[str] = set()
    kept: list[dict[str, Any]] = []
    dropped: list[str] = []
    for item in results:
        if not isinstance(item, dict):
            kept.append(item)
            continue
        name = (item.get("name") or "").strip().casefold()
        if not name:
            kept.append(item)
            continue
        if name in seen:
            dropped.append(str(item.get("key") or ""))
            continue
        seen.add(name)
        kept.append(item)
    return kept, dropped


def _apply_priority_and_dedupe(
    response: dict[str, Any], spec: PrioritySpec, dedupe: bool
) -> dict[str, Any]:
    """Sort response.results by priority, then optionally dedupe by name. Returns
    a shallow copy with a new `results` list — never mutates the cached response.
    When dedupe drops items, their keys are surfaced as `dropped_variants` so the
    caller can still fetch them explicitly via the matching `get_*` tool."""
    if not isinstance(response, dict):
        return response
    results = response.get("results")
    if not isinstance(results, list):
        return response
    ranked = _sort_by_priority(results, spec)
    if not dedupe:
        return {**response, "results": ranked}
    kept, dropped = _dedupe_by_name(ranked)
    out = {**response, "results": kept}
    if dropped:
        out["dropped_variants"] = dropped
    return out


# --- Filter helpers ----------------------------------------------------------
# These are the building blocks every search_* function uses to translate Pythonic
# kwargs into Open5e v2 query params. Open5e's filterset is Django-style: a field
# `foo` with comparators uses `foo__lt`, `foo__lte`, `foo__gt`, `foo__gte`, and a
# multi-value filter uses `foo__in` with a comma-separated string. Booleans accept
# Python True/False (requests serializes to "True"/"False", which the API accepts).

def _add_range(
    extra: dict[str, Any],
    base: str,
    lo: Optional[float] = None,
    hi: Optional[float] = None,
) -> None:
    """Inclusive numeric range: maps (lo, hi) to base__gte / base__lte."""
    if lo is not None:
        extra[f"{base}__gte"] = lo
    if hi is not None:
        extra[f"{base}__lte"] = hi


def _add_bool(extra: dict[str, Any], key: str, value: Optional[bool]) -> None:
    """Add boolean filter only when explicitly set (None means 'don't filter')."""
    if value is not None:
        extra[key] = value


def _add_keys(extra: dict[str, Any], keys: Optional[str]) -> None:
    """Batch-fetch by a comma-separated list of record keys (key__in)."""
    if keys:
        extra["key__in"] = keys


def _add_has_fields(
    extra: dict[str, Any], comma_list: Optional[str], template: str
) -> None:
    """For the Open5e `__isnull` boolean trick: a comma list of field names becomes
    a series of `template.format(field)__isnull=False` filters. Used for matching
    creatures that have specific saving throws or skill proficiencies."""
    if not comma_list:
        return
    for f in (s.strip() for s in comma_list.split(",")):
        if f:
            extra[template.format(f) + "__isnull"] = False


# --- Creatures (formerly v1 monsters) -----------------------------------------

def search_monsters(
    name: Optional[str] = None,
    cr: Optional[str] = None,
    cr_min: Optional[float] = None,
    cr_max: Optional[float] = None,
    type: Optional[str] = None,
    size: Optional[str] = None,
    ac_min: Optional[int] = None,
    ac_max: Optional[int] = None,
    strength_min: Optional[int] = None,
    strength_max: Optional[int] = None,
    dexterity_min: Optional[int] = None,
    dexterity_max: Optional[int] = None,
    constitution_min: Optional[int] = None,
    constitution_max: Optional[int] = None,
    intelligence_min: Optional[int] = None,
    intelligence_max: Optional[int] = None,
    wisdom_min: Optional[int] = None,
    wisdom_max: Optional[int] = None,
    charisma_min: Optional[int] = None,
    charisma_max: Optional[int] = None,
    passive_perception_min: Optional[int] = None,
    passive_perception_max: Optional[int] = None,
    has_saves: Optional[str] = None,
    has_skills: Optional[str] = None,
    keys: Optional[str] = None,
    source: Optional[str] = None,
    match: str = "partial",
    fields: Optional[str] = None,
    exclude: Optional[str] = None,
    ordering: Optional[str] = None,
    limit: int = 10,
    dedupe: bool = True,
) -> dict[str, Any]:
    filter_source, spec = _resolve_source(source, DEFAULT_PRIORITY_SRD)
    extra: dict[str, Any] = {}
    if cr is not None:
        extra["challenge_rating"] = cr
    _add_range(extra, "challenge_rating", cr_min, cr_max)
    if type:
        # Open5e's /v2/creatures/ filterset uses bare `type` and `size` (not the
        # __key suffix that other endpoints use — it's inconsistent per-endpoint).
        extra["type"] = type
    if size:
        extra["size"] = size
    _add_range(extra, "armor_class", ac_min, ac_max)
    _add_range(extra, "ability_score_strength", strength_min, strength_max)
    _add_range(extra, "ability_score_dexterity", dexterity_min, dexterity_max)
    _add_range(extra, "ability_score_constitution", constitution_min, constitution_max)
    _add_range(extra, "ability_score_intelligence", intelligence_min, intelligence_max)
    _add_range(extra, "ability_score_wisdom", wisdom_min, wisdom_max)
    _add_range(extra, "ability_score_charisma", charisma_min, charisma_max)
    _add_range(extra, "passive_perception", passive_perception_min, passive_perception_max)
    # `has_saves='dexterity,wisdom'` finds creatures with proficiency in those saves
    # (i.e. saving_throw_X is not null). Same trick for skills.
    _add_has_fields(extra, has_saves, "saving_throw_{}")
    _add_has_fields(extra, has_skills, "skill_bonus_{}")
    _add_keys(extra, keys)
    params = _build_query(
        name=name, match=match, source=filter_source, fields=fields, exclude=exclude,
        ordering=ordering, limit=limit, extra=extra,
        endpoint="/v2/creatures/",
    )
    return _apply_priority_and_dedupe(_api_get("/v2/creatures/", params), spec, dedupe)


def get_monster_details(key: str) -> dict[str, Any]:
    return _api_get(f"/v2/creatures/{key}/")


